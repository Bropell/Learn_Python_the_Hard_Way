days = "Mon Tue Wed Thu Fri Sat Sun"
# Assign the variable months to the string "\nJan\nFeb\nMar\nApr\nMay\nJun\nJul\nAug". The \n puts what follows on a new line.
months = "\nJan\nFeb\nMar\nApr\nMay\nJun\nJul\nAug"

# Prints "Here are the days: Mon Tue Wed Thu Fri Sat Sun" 
print("Here are the days: ", days)
# Prints "Here are the months: Jan Feb Mar Apr May Jun Jul Aug" with each month on its own line
print("Here are the months: ", months)

